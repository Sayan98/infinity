#include "Integer.h"
#include <iostream>

int main() {
    Integer a = 3;
    Integer b = 2;

    std::cout << "a :: " << a;
    std::cout << "b :: " << b;
    std::cout << "a + b :: " << a + b;
    std::cout << "a - b :: " << a - b;
    std::cout << "a * b :: " << a * b;
    std::cout << "a / b :: " << a / b;

    return 0;
}
